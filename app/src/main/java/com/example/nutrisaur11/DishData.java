        // Helper methods for nutritional analysis
        public boolean isHighProtein() {
            return protein >= 20.0; // 20g+ protein per serving
        }
        
        public boolean isHighIron() {
            return iron >= 3.0; // 3mg+ iron per serving
        }
        
        public boolean isHighCalcium() {
            return calcium >= 200.0; // 200mg+ calcium per serving
        }
        
        public boolean isHighVitaminA() {
            return vitaminA >= 1000.0; // 1000 IU+ vitamin A per serving
        }
        
        public boolean isHighVitaminC() {
            return vitaminC >= 30.0; // 30mg+ vitamin C per serving
        }
        
        public boolean isHighFiber() {
            return fiber >= 5.0; // 5g+ fiber per serving
        }
        
        public boolean isLowCalorie() {
            return calories <= 200; // 200 calories or less per serving
        }
        
        public boolean isEnergyDense() {
            return calories >= 300; // 300+ calories per serving
        }
        
        public double getNutritionalScore() {
            double score = 0.0;
            
            // Protein score (max 25 points)
            if (protein >= 25.0) score += 25.0;
            else if (protein >= 20.0) score += 20.0;
            else if (protein >= 15.0) score += 15.0;
            else if (protein >= 10.0) score += 10.0;
            else if (protein >= 5.0) score += 5.0;
            
            // Iron score (max 20 points)
            if (iron >= 5.0) score += 20.0;
            else if (iron >= 3.0) score += 15.0;
            else if (iron >= 2.0) score += 10.0;
            else if (iron >= 1.0) score += 5.0;
            
            // Vitamin A score (max 15 points)
            if (vitaminA >= 2000.0) score += 15.0;
            else if (vitaminA >= 1000.0) score += 10.0;
            else if (vitaminA >= 500.0) score += 5.0;
            
            // Vitamin C score (max 15 points)
            if (vitaminC >= 60.0) score += 15.0;
            else if (vitaminC >= 30.0) score += 10.0;
            else if (vitaminC >= 15.0) score += 5.0;
            
            // Fiber score (max 10 points)
            if (fiber >= 8.0) score += 10.0;
            else if (fiber >= 5.0) score += 7.0;
            else if (fiber >= 3.0) score += 5.0;
            
            // Calcium score (max 10 points)
            if (calcium >= 300.0) score += 10.0;
            else if (calcium >= 200.0) score += 7.0;
            else if (calcium >= 100.0) score += 5.0;
            
            // Omega-3 score (max 5 points)
            if (omega3 >= 1.0) score += 5.0;
            else if (omega3 >= 0.5) score += 3.0;
            else if (omega3 >= 0.2) score += 1.0;
            
            return score;
        }
        
        public String getNutritionalSummary() {
            StringBuilder summary = new StringBuilder();
            
            if (isHighProtein()) summary.append("High Protein ");
            if (isHighIron()) summary.append("Iron-Rich ");
            if (isHighCalcium()) summary.append("Calcium-Rich ");
            if (isHighVitaminA()) summary.append("Vitamin A-Rich ");
            if (isHighVitaminC()) summary.append("Vitamin C-Rich ");
            if (isHighFiber()) summary.append("High Fiber ");
            if (isLowCalorie()) summary.append("Low Calorie ");
            if (isEnergyDense()) summary.append("Energy Dense ");
            
            return summary.toString().trim();
        }
